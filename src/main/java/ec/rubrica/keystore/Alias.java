package ec.rubrica.keystore;

/**
 * Alias de un certificado dentro de un KeyStore.
 * 
 * @author Ricardo Arguello <ricardo@rubrica.ec>
 */
public class Alias {

	private String alias;
	private String name;

	public Alias(String alias, String name) {
		this.alias = alias;
		this.name = name;
	}

	public String getAlias() {
		return alias;
	}

	public String getName() {
		return name;
	}

	public String toString() {
		return name;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((alias == null) ? 0 : alias.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Alias other = (Alias) obj;
		if (alias == null) {
			if (other.alias != null)
				return false;
		} else if (!alias.equals(other.alias))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}
}